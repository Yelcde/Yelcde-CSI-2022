package array;

public class PrimeiroArray {
    public static void main(String[] args) {
        int[] numeros = new int[4];
        numeros[0] = 8;
        numeros[1] = 2;
        numeros[2] = 9;
        numeros[3] = 2;

        // int[] numeros = {8, 2, 9, 2}

        System.out.println(numeros.length);
        System.out.println(numeros[0]);
        System.out.println(numeros[1]);
        System.out.println(numeros[2]);
    }
}
